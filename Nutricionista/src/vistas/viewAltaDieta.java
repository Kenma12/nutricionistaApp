    private void filaSeleccionada(){
        tblDietas.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()){
                    int i = tblDietas.getSelectedRow();
                    if (i != -1){
                        String nombreDieta = tblDietas.getValueAt(i, 1).toString();
                        txtModNombre.setText(nombreDieta);
                        ArrayList<String> textos = new ArrayList<>();
                        textos.addAll(dietaComidaData.getComidas((int) tblDietas.getValueAt(i, 0)));
                        //cargarTextos(textos);
                    }
                }
            } 
        });
    }
    
    private void cargarTextos(ArrayList<String> textos){
        txtDesayuno.setText(textos.get(0));
        txtAlmuerzo.setText(textos.get(1));
        txtMerienda.setText(textos.get(2));
        txtCena.setText(textos.get(3));
        txtSnack.setText(textos.get(4));
    }
    
    private void cargarDatos(){
        pacientes.clear();
        pacientes.addAll(pacienteData.listarPacientes());
        CBPacientes.removeAllItems();
        CBPacientes.addItem("Seleccionar Paciente");
        for (Paciente p:pacientes){
            CBPacientes.addItem(p.toString());
        }
        comidas.clear();
        comidas.addAll(comidaData.listarComidas());
        CBDesayuno.removeAllItems();
        CBDesayuno.addItem("Seleccionar Desayuno");
        
        CBAlmuerzo.removeAllItems();
        CBAlmuerzo.addItem("Seleccionar Almuerzo");
        
        CBMerienda.removeAllItems();
        CBMerienda.addItem("Seleccionar Merienda");
        
        CBCena.removeAllItems();
        CBCena.addItem("Seleccionar Cena");
        
        CBSnack.removeAllItems();
        CBSnack.addItem("Seleccionar Snack");
        
        for(Comida c: comidas){
            CBDesayuno.addItem(c.toString());
            CBAlmuerzo.addItem(c.toString());
            CBMerienda.addItem(c.toString());
            CBCena.addItem(c.toString());
            CBSnack.addItem(c.toString());
        }
    }

    private void limpiarDatos(){
        txtNombre.setText("");
        txtPesoIn.setText("");
        CBPacientes.setSelectedIndex(0);
        CBDesayuno.setSelectedIndex(0);
        CBAlmuerzo.setSelectedIndex(0);
        CBMerienda.setSelectedIndex(0);
        CBCena.setSelectedIndex(0);
        CBSnack.setSelectedIndex(0);
    }
    
    private void cargarDietas(){
        dietas = new ArrayList<>();
        dietas.addAll(dietaData.listarDietas());
        for (Dieta d:dietas){
            modelo.addRow(new Object[]{d.getIdDieta(), d.getNombreDieta(), d.getPaciente().getIdPaciente(),
            d.getFechaInicial().toString(), d.getFechaFinal().toString(), d.getPesoInicial(), d.getPesoFinal(), d.isDietaTerminada()});
        }
    }
    
    private void armarTabla(){
        modelo.addColumn("id");
        modelo.addColumn("Nombre");
        modelo.addColumn("idPaciente");
        modelo.addColumn("Fecha Inicial");
        modelo.addColumn("Fecha Final");
        modelo.addColumn("Peso Inicial");
        modelo.addColumn("Peso Final");
        modelo.addColumn("terminada");
        tblDietas.setModel(modelo);
    }
    
